    private void poligonoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_poligonoMouseClicked
        seleccionado = !seleccionado;
        Icon select = new ImageIcon(getClass().getResource("../images/selectPoli.png"));
        Icon Unselect = new ImageIcon(getClass().getResource("../images/unselectPoli.png"));
        setSeleccionado(seleccionado);
        if (seleccionado) {
            poligono.setSelected(true);
            poligono.setBackground(Color.BLUE); // Por ejemplo, cambiar el color de fondo a azul
            numLados.setEnabled(false);
            poligono.setIcon(Unselect);
        } else {
            poligono.setSelected(false);
            poligono.setBackground(Color.GRAY);
            numLados.setEnabled(true);
            poligono.setIcon(select);
          
        }
    }//GEN-LAST:event_poligonoMouseClicked
